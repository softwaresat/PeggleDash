Hole::Hole(){
    x = 0;
    y = 160*256;
    vx = 512;
    h = 16;
    w = 16;
    image = HoleSprite;
}
void Hole::reset(){
    x = 0;
    y = 160*256;
}

int32_t Hole::getX(){
    return x;
}

int32_t Hole::getY(){
    return y;
}

void Hole::moveHole(){
     x += vx;

    if(x/256 >= 128 || x <= 0){
        vx = -vx;
    }
}

